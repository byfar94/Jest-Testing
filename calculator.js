export { add, subtract, multiply, divide };

const add = function (a, b) {
  let ans = a + b;
  return ans;
};

const subtract = function (a, b) {
  let ans = a - b;
  return ans;
};

const multiply = function (a, b) {
  let ans = a * b;
  return ans;
};

const divide = function (a, b) {
  let ans = a / b;
  return ans;
};
